                           <h3 class="pb-2 f600">1) How common is arthritis and can it be prevented?</h3>
                           <p class="pb-4">Arthritis is one of the most common ailments seen in the country. This disease is difficult to control because it is caused primarily by increasing age and family history. However, one can reduce the risks of developing arthritis by exercising regularly and maintaining a healthy lifestyle. </p>

                           <h3 class="pb-2 f600">2) How can I diagnose myself for arthritis?</h3>
                           <p class="pb-2">While a proper diagnosis can only be given by a trained physician, there are symptoms that you can look out for that may be due to arthritis:</p>
                           <ul class="pb-2 ml-3">
                            <li> Joint pain (especially in the fingers, feet and knees)</li>
                            <li> Joint swelling</li>
                            <li> Fatigue</li>
                            <li> Joint redness and warmth</li>
                            <li> Joint deformity</li>
                            <li> Loss of range of motion of joints</li>
                            <li> Involvement of multiple joints</li>
                            <li> Loss of joint function</li>
                            <li> Joint stiffness</li>
                            <li> Limping</li>
                            <li> The joints on both sides are affected</li>
                            <li> Fever</li>
                            <li> Anaemia</li>
                           </ul>
                           <p class="pb-4">If you notice any one or more of the above symptoms, consult your doctor immediately.  </p>

                           <h3 class="pb-2 f600">3) Is Arthritis Genetic?</h3>
                           <p class="pb-4">Primary osteoarthritis is a degenerative disorder due to ageing process. In some cases, arthritis is an auto-immune disorder that develops due to genetic & environmental factors. While it is still unclear what is the exact cause of arthritis but you have a higher risk of developing arthritis if a close relative has arthritis. </p>

                           <h3 class="pb-2 f600">4) What treatment procedures are there for arthritis?</h3>
                           <p class="pb-2">As of now, there is no cure for arthritis. However, there are drugs and physical therapy regimen that helps with the pain and reduces the chances of further inflammation.</p>
                           <p class="pb-2">According to the severity of your condition, your doctor may prescribe the following:</p>
                           <ul class="pb-4 ml-3">
                              <li>Drugs that reduce the pain: Painkillers such as paracetamol & NSAID’s (Non-Steroidal Anti-Inflammatory Drugs). Steroids may be given in injectable or tablet forms for a short period of a time.</li>
                              <li>DMARD’s (Disease Modifying Anti Rheumatic Drugs) & biological response modifiers are used to suppress the inflammatory agents of arthritis.</li>
                              <li>Physical therapy is an important aspect in the treatment of arthritis. Hydrotherapy, physiotherapy and occupational therapy helps in regaining your range of motion and doing daily household work with arthritis.</li>
                           </ul>

                           <h3 class="pb-2 f600">5) When Is the right time to undergo a knee replacement?</h3>
                           <p class="pb-4">There is no precise formula for deciding when you should have a knee replacement. The main reason to have it done is pain, but if you have tried all other forms of non-operative treatment including lifestyle remedies, anti-inflammatory medication, physical therapy, and injections it may be time to think about surgery.</p>

                           <h3 class="pb-2 f600">6) Can I avoid surgery?</h3>
                           <p class="pb-2">Before you consider surgery, your doctor will usually encourage you to try various non-surgical treatments. These may include:</p>
                           <ul class="pb-2 ml-3">
                              <li> physical therapy</li>

                              <li> weight loss (if appropriate)</li>

                              <li> anti-inflammatory medication</li>

                              <li> steroid injections</li>

                              <li> hyaluronic (gel) injections</li>

                              <li> PRP injection</li>
                           </ul>
                           <p class="pb-4">In some cases, these solutions may help manage knee problems. However, if the symptoms become worse and start to affect your quality of life, surgery may be the best option.</p>

                           <h3 class="pb-2 f600">7) What happens during surgery, and how long does it take?</h3>
                           <ul class="ml-3 pb-4">
                            <li> The surgeon will make an incision over the front of your knee to expose the damaged area of your joint.</li>

                            <li> The standard incision size varies from approximately 6-7 inches in length.</li>

                            <li> During the operation, the surgeon moves your kneecap to the side and cuts away the damaged cartilage and a small amount of bone.</li>

                            <li> They then replace the damaged tissue with new metal and plastic components.</li>

                            <li> The components combine to form an artificial joint that is biologically compatible and mimics the movement of your natural knee.</li>

                            <li> Most knee replacement procedures take 60 to 90 minutes to complete.</li>
                           </ul>

                           <h3 class="pb-2 f600">8) Should I worry about anaesthesia?</h3>
                           <p class="pb-2">Any operation done with anaesthesia has risks, although it is rare that severe complications result from any type of anaesthesia.</p>
                           <p class="pb-2">The options for TKR include:</p>
                           <ul class="pb-2 ml-3">
                              <li> General anaesthesia</li>

                              <li> Spinal or epidural</li>

                              <li> A regional nerve block anaesthesia</li>
                           </ul>
                           <p class="pb-4">An anaesthesia team will decide on the most suitable options for you but most knee replacement surgery is done using a combination of the above.</p>

                           <h3 class="pb-2 f600">9) How much pain will I have after surgery?</h3>
                           <ul class="pb-4 ml-3">
                             <li> There will definitely be some pain after your operation but your surgery team will do everything possible to keep it manageable and minimal.</li>

                              <li> You may receive a nerve block prior to your operation and your surgeon may also use a long-acting local anaesthetic during the procedure to help with pain relief after the procedure.</li>

                              <li> Your doctor will prescribe medication to help you manage the pain. You may receive this intravenously (IV) immediately after surgery.</li>

                              <li> When you leave the hospital, the doctor will give you pain relief medication as pills or tablets.</li>
                           </ul>

                           <h3 class="pb-2 f600">10) What can I expect during recovery and rehabilitation?</h3>
                           <ul class="pb-4 ml-3">
                              <li>Most people are up and walking within 48 hours with the aid of a walker or crutches.</li>

                              <li>Following your operation, a physical therapist will help you bend and straighten your knee, get you out of bed, and teach you to walk with the walker. This is often done on the same day or next day of your operation.</li>

                              <li>Most patients are discharged from the hospital 3-4 days after surgery.</li>

                              <li>After you return home, therapy will continue regularly for several weeks. Specific exercises will aim to improve the functionality of the knee.</li>

                              <li>Most people recover within 3 months, although it may take 6 months or longer for some people to recover fully.</li>
                           </ul>

                           <h3 class="pb-2 f600">11) How long will the artificial knee joint last?</h3>
                           <p class="pb-4">In 80–90% of people who have total knee replacement, the joint lasts for about 15-20 years.</p>
